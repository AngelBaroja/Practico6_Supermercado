    private void minimoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_minimoKeyTyped
        borrarFilasTablas();
        maximo.setText("");
        habilitarTextField();
        char c = evt.getKeyChar();
        // Verificar si el carácter no es un número o si es el carácter de borrado
        if (!Character.isDigit(c) && c != KeyEvent.VK_BACK_SPACE) {
            evt.consume();  // Evitar que el carácter no numérico sea ingresado
        }
    }//GEN-LAST:event_minimoKeyTyped

