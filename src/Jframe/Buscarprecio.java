    private void maximoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_maximoKeyReleased
        borrarFilasTablas();
        try {
            int min = Integer.parseInt(minimo.getText());
            int max = Integer.parseInt(maximo.getText());
            TreeSet<Producto> productos = Supermercado_DeTodo.buscarXprecio(min, max);
            mostrarProductosEnTabla(productos);
        } catch (NumberFormatException e) {
            System.out.println("Los valores ingresados no son números válidos.");
        }
        char c = evt.getKeyChar();
        // Verifica si el carácter no es un número o si es el carácter de borrado
        if (!Character.isDigit(c) && c != KeyEvent.VK_BACK_SPACE) {
            evt.consume();
        }
    }//GEN-LAST:event_maximoKeyReleased
    public void armartabla(){
        modelo.addColumn("Codigo");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Precio");
        modelo.addColumn("Categoria");
        modelo.addColumn("Stock");
        jTable.setModel(modelo);
    }
    private DefaultTableModel modelo = new DefaultTableModel(){
        @Override
        public boolean isCellEditable(int fila, int columna){
            return false;
        }
    };
    private void borrarFilasTablas(){
        int fila= modelo.getRowCount()-1;
        for (int i = fila ; i >= 0 ; i--) {
            modelo.removeRow(i);
        }
    }
    private void habilitarTextField() {
        String texto = minimo.getText();
        // Si hay texto en el primer campo, habilitamos el segundo
        if (!texto.isEmpty()) {
            maximo.setEnabled(true);
        } else {
            // Si está vacío, deshabilitamos el segundo
            maximo.setEnabled(false);
        }
    }
    private void mostrarProductosEnTabla(TreeSet<Producto> productos) {  
        for (Producto producto : productos) {
            Object[] fila = new Object[] {
                producto.getCodigo(),
                producto.getDescripcion(),
                producto.getPrecio(),
                producto.getRubro(),
                producto.getStock()
            };
            modelo.addRow(fila);
        }
}
    

